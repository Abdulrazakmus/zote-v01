            <div class="page-summary__left">
              <p class="intro">
                Things To Do </p>


              <br>
              <p>
                <b>Boat cruises</b> <br>
                You’ll experience the thrill of walking safaris accompanied by expert trackers and naturalist guides, something not possible in northern Tanzania’s more famous national parks. Walking safaris explore the little details easily missed from a safari truck; animal prints in the dust, fresh droppings and subtly snapped vegetation – all the while bringing you (if you’re lucky) heart-thumping close to herds of elephants or lazy packs of lions.
              </p>
              <br>
              <p>
                <b>Walking safaris</b> <br>
                Selous’ network of waterways is key to its charm, offering a refreshing alternative to sometimes-bone-shaking 4x4 game drives. Cruises on the Rufiji River offer a relaxing, peaceful way to spot elephants and other game drawn to the water. Sunset cruises to the sounds of hippos snorting are a special way to end the day – and something you won’t find in the Serengeti
              </p>
              <br>

              <p>
                <b>Game drives</b> <br>
                Morning game drives in 4x4 safari vehicles start early, as the mists lift and wildlife make the most of cooler temperatures to forage and hunt. You’ll be lucky to spot wild dogs, lions, elephants, endangered black rhino and more. Unlucky if you share any of your sightings with other trucks. Night time safaris are also possible, when rustling vegetation and glowing eyes hint at the animals hiding in the dark.
              </p>
              <br>

              <p>
                <b>Masaai community visits</b> <br>
                While you’ll travel to Selous for the wildlife, don’t discount the region’s human residents. Maasai villages dot the edges of the reserve, and many lodges have fostered community links which support local people and allow visitors an insight into the Masaai’s unique bush knowledge and tribal way of life.
              </p>


