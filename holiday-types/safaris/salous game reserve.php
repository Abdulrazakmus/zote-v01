                <br> THIS IS THE PARK TO VISIT IN SAFARI HIGH SEASON. EVEN AT ITS MOST POPULAR, RELATIVELY SMALL VISITOR NUMBERS MEAN SELOUS RETAINS ITS UNTOUCHED WILDERNESS FEEL.
