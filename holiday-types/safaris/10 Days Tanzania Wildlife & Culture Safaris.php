              <p>
                The great migration is when a million wildebeest, each one driven by the same ancient rhythm, fulfill its instinctive role in the inescapable cycle of life. It is a frenzied 3-week bout of territorial conquests and mating. It is survival of the fittest as 40km (25 miles) long columns plunge through crocodile-infested waters on the annual exodus north, replenishing the species in a brief population explosion that produces more than 8,000 calves daily. Then, the 1,000km (600 miles) pilgrimage begins again.

