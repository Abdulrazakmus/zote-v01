            <p class="intro" style="margin-top: 50px;">

              Dive into Adventure: Explore Zanzibar's East Coast Oasis at Dongwe Beach and Blue Lagoon
            <p>

              Enjoy an amazing snorkeling experience in the Best Lagoon of the East Coast of Zanzibar! Visit Dongwe Beach where you will onboard in your sailing outriggers on the way to Blue Lagoon. Swim among hundreds of fishes, and explore colorful starfish, nice corals and some sea lions. </p>

            <br>


            <p>

              Once you have the snorkeling gear and feel comfortable you will make your way out to Blue Lagoon Atoll on a motorized boat. Your first snorkeling location will be guided through deep waters, home of huge schools of fish and wonderful coral reefs. Next you will go to a shallower location where the marine life and vegetation live closer to the surface and you will enjoy observing from closer range. You will also spend your own time at the sandbank before driving back to mark the ends of your adventure.


            </p>

            <br>
            <p>

              Join us for your Group Private Swahili lunch before driving to Kuza Caze for another historic experience.
            </p>

            <br>
            <p>
              Kuza Cave offers a unique experience for adventurous nature-lovers. Discover the ancient limestone cave and crystal clear healing mineral water pool. See artifacts from the cave and a timeline of Swahili history going back to the beginning of our story as humans originating in East Africa.

            </p>


            <br>
            <p>
              You will walk across a small bridge and a few steps to the base of the well-lit cave to find a large pool formed by a subterranean river. The area is a wildlife sanctuary for dozens of monkeys, birds, butterflies and bushbabies. Our Culture Centre invites you to enjoy Swahili music, drumming and dancing with our onsite artists. Stay for the day and learn the secrets to Swahili cooking.

            </p>


            <br>
            <p>
              You will learn how to gently blend Zanzibar spices and make fresh coconut milk curry. Don't miss out on discovering this hidden gem tucked away in the tropical jungle of Jambiani - you will be surprised!
            </p>



            <br>
            <p>
              Over the end of this historic experience will take a drive to Mtende Beach (The hidden gem) is located between two huge rocky barriers, which makes it one of the most quiet and warm rocky beaches even in the coldest seasons. The beauty of the beach is not limited to these barriers, it also contains huge trees that are almost over a thousand years old, and its waters are emerald green on this beach swimming, oating and many more can be done. After relaxing and enjoying the beach we will drive back to the hotel which marks the end of our tour.
            </p>
